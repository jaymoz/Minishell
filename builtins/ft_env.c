#include "../minishell.h"

void	ft_env(char **envs)
{
	int	i;

	i = 0;
	while (envs[i])
	{
		write(STDOUT, envs[i], ft_strlen(envs[i]));
		write(STDOUT, "\n", 1);
		i++;
	}
}
